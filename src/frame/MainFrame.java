    private void tbtransMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbtransMouseClicked
        // TODO add your handling code here:
        int baris = tbtrans.getSelectedRow();
        if(baris != -1){
            SimpleDateFormat date = new SimpleDateFormat("yyyy-MM-dd");
            Date dateFormat = null;
            try {
                dateFormat = date.parse(tbtrans.getValueAt(baris, 1).toString());
            } catch (ParseException ex) {
                Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
            txttanggal.setDate(dateFormat);
            String trans = tbtrans.getValueAt(baris, 2).toString();
            if(trans.equals("Pemasukan"))
                masuk.setSelected(true);
            else 
                keluar.setSelected(true);
            
            txtjumlah.setText(tbtrans.getValueAt(baris, 3).toString());
            txtket.setText(tbtrans.getValueAt(baris, 4).toString());
        }
    }//GEN-LAST:event_tbtransMouseClicked

