    public void selectData(){
        String kolom[] = {"no","tanggal","transaksi","jumlah","keterangan","saldo"};
        DefaultTableModel dtm = new DefaultTableModel(null, kolom);
        String SQL = "SELECT * FROM transaksi";
        ResultSet rs = KoneksiDB.executeQuery(SQL);
        try{
            while(rs.next()){
                String no = rs.getString(1);
                String tanggal = rs.getString(2);
                String transaksi = rs.getString(3);
                String jumlah = rs.getString(4);
                String keterangan = rs.getString(5);
                String saldo = rs.getString(6);
                String data[] = {no,tanggal,transaksi,jumlah,keterangan,saldo};
                dtm.addRow(data);
            }
        }catch(SQLException ex){
            Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        tbtrans.setModel(dtm);
    }
    
    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat dateFormat = new SimpleDateFormat ("yyyy-MM-dd");
        String tanggal = dateFormat.format(txttanggal.getDate());
        String sql = "SELECT * FROM transaksi";
        ResultSet rs = KoneksiDB.executeQuery(sql);
        String sal = "0";
        try{
            while(rs.next()){
                sal = rs.getString(6);
            }
        }catch(SQLException ex){
            Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        int saldo = Integer.parseInt(sal);
        String trans = "";
        int uang = Integer.parseInt(txtjumlah.getText());
        String saldofinal = "";
                if (masuk.isSelected()){
                    int saldoa = saldo + uang;
                    saldofinal = Integer.toString(saldoa);
                    trans = "Pemasukan";
                    
                }else{
                    int saldoa = saldo - uang;
                    saldofinal = Integer.toString(saldoa);
                    trans = "Pengeluaran";
                }
        String ket  = txtket.getText();
        
        String SQL = "INSERT INTO transaksi(tanggal,transaksi,jumlah,keterangan,saldo)"+
                    "VALUES('"+tanggal+"',"+ "'"+trans+"',"+ "'"+uang+"',"+ "'"+ket+"',"+ "'"+saldofinal+"')";
            int status = KoneksiDB.execute(SQL);
            if(status == 1){
                JOptionPane.showMessageDialog(this, "Data Berhasil Ditambahkan","Sukses",JOptionPane.INFORMATION_MESSAGE);
                selectData();
            }else{
                JOptionPane.showMessageDialog(this, "Data Gagal Ditambahkan","Coba Lagi",JOptionPane.INFORMATION_MESSAGE);
            }
        
    }//GEN-LAST:event_simpanActionPerformed

